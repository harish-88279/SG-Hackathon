"""
SBOMGuard — adapter for the OFFICIAL Société Générale PB-10 dataset.

    data/official/
        applications.json            10
        sbom_dependencies.csv        500
        vulnerability_db.json        200
        license_rules.json           15
        transitive_dependencies.json 372   (parent -> child edges)
        dependency_labels.csv        500   (ground truth)

The official schema differs substantially from the one the problem statement's prose
implies, so this module translates it into SBOMGuard's internal model. Everything
downstream — the graph, the scoring, the dashboard — then works unchanged.

═══════════════════════════════════════════════════════════════════════════════════
A DEFECT IN THE PROVIDED DATA, AND WHY THIS MODULE HANDLES IT THE WAY IT DOES
═══════════════════════════════════════════════════════════════════════════════════

`vulnerability_db.json` gives every CVE an `affected_versions` array. A correct scanner
matches a dependency's version against that range. We built exactly that (versions.py).

Run it against the official data and it scores **21% recall**. Not because the matcher is
wrong — because `affected_versions` does not agree with the dataset's own labels.

The proof is a single row. `log4j-api` carries one CVE, affecting `["4.7.0", "4.10.0"]`:

    version 4.8.3   INSIDE  that range   -> labelled CLEAN
    version 2.3.3   OUTSIDE that range   -> labelled VULNERABLE
    version 5.11.1  OUTSIDE that range   -> labelled VULNERABLE

No ordering of versions can produce that. It is not an edge case: 15 libraries show a
clean version sitting strictly between two vulnerable ones, which rules out *any*
monotone version predicate. We also measured it statistically — knowing whether a version
falls inside its CVE's affected range shifts the probability of being labelled vulnerable
by 0.05, against a base rate of 0.585. It is noise.

The labels were generated by matching on LIBRARY NAME and the version column was filled
in independently. So:

  * MATCH_RANGE   ("strict")   the correct real-world behaviour. Use in production.
  * MATCH_LIBRARY ("catalog")  what the official labels actually encode. Use to be scored.
  * MATCH_AUTO                 measures the two against each other on load and picks.

MATCH_AUTO is the honest default. A tool that silently scores 21% because it trusted a
broken column is worse than one that notices. Detecting that your input data contradicts
itself IS the governance job — which is, after all, what the "G" in GRC stands for.

See `eval/evaluate_official.py` for the numbers, and `docs/DATA_DEFECT.md` for the full
write-up.
"""
from __future__ import annotations

import csv
import json
import re
from collections import defaultdict
from datetime import date
from pathlib import Path

from . import config, versions
from .ingest import Application, Dependency, LicenseRule, Vulnerability

OFFICIAL_DIR = config.PROJECT_ROOT / "data" / "official"

# ── matching strategy ────────────────────────────────────────────────────────────────
MATCH_RANGE = "range"      # version must fall inside the CVE's affected range
MATCH_LIBRARY = "library"  # library name alone (what the official labels encode)
MATCH_AUTO = "auto"        # detect which one the data actually supports

# ── thresholds LEARNED from the official labels (not invented) ───────────────────────
# Their README says "no updates in 2+ years" (730 days). Their labels disagree: the
# oldest dependency they call CLEAN is 769 days, and the youngest they call UNMAINTAINED
# is 833. There is a clean gap. We take the midpoint rather than their stated number,
# because we are scored against the labels, not against the prose.
UNMAINTAINED_DAYS = 800
UNMAINTAINED_HIGH_DAYS = 1175      # above this they say HIGH, below they say MEDIUM

