# ══════════════════════════════════════════════════════════════════════ loading
def _read_csv(path: Path) -> list[dict]:
    """Their CSVs ship as cp1252, not UTF-8. A plain open() raises UnicodeDecodeError on
    the em-dash in the explanation column. Small thing; breaks a naive loader on line 1."""
    for enc in ("utf-8-sig", "utf-8", "cp1252", "latin-1"):
        try:
            with path.open(encoding=enc, newline="") as f:
                return list(csv.DictReader(f))
        except UnicodeDecodeError:
            continue
    raise UnicodeDecodeError("could not decode", b"", 0, 1, str(path))


CRITICALITY_WEIGHT = {"CRITICAL": 1.5, "HIGH": 1.25, "MEDIUM": 1.0, "LOW": 0.75}


def load_applications(d: Path = OFFICIAL_DIR) -> list[Application]:
    raw = json.loads((d / "applications.json").read_text(encoding="utf-8"))
    out = []
    for a in raw:
        proprietary = a.get("license_model") == "proprietary"
        out.append(Application(
            app_id=a["app_id"],
            name=a["name"],
            business_criticality=a.get("criticality", "MEDIUM"),
            criticality_weight=CRITICALITY_WEIGHT.get(a.get("criticality"), 1.0),
            environment="production",
            # Their licence rule fires on `license_model == proprietary` alone; there is no
            # distribution flag. So a proprietary app is treated as distributed, which makes
            # our (stricter, context-aware) engine agree with their taxonomy.
            internet_facing=a.get("deployment") == "cloud",
            distributed=proprietary,
            proprietary=proprietary,
            handles_pii=False,
            handles_cardholder_data=a.get("department") == "Finance",
            ecosystem={"Java": "maven", "Python": "pypi", "JavaScript": "npm",
                       "Go": "go"}.get(a.get("language", ""), "unknown"),
            team=a.get("department", ""),
            owner=a.get("business_owner", ""),
            description=f"{a.get('language','')} · {a.get('deployment','')}",
        ))
    return out


def load_dependencies(d: Path = OFFICIAL_DIR) -> list[Dependency]:
    rows = _read_csv(d / "sbom_dependencies.csv")

    # transitive_dependencies.json is the real edge list. Index child -> parent so we can
    # attach every transitive node to the thing that actually pulled it in.
    edges = json.loads((d / "transitive_dependencies.json").read_text(encoding="utf-8"))
    parent_of: dict[tuple[str, str], str] = {}
    for e in edges:
        parent_of[(e["application_id"], e["child_library"])] = e["parent_library"]

    out = []
    for r in rows:
        app_id = r["application_id"]
        lib = r["library"]
        is_transitive = r["dependency_type"] == "transitive"
        out.append(Dependency(
            dependency_id=r["dep_id"],
            app_id=app_id,
            app_name=r.get("application_name", ""),
            library_name=lib,
            version=r["version"],
            ecosystem="unknown",
            license=(r.get("license") or "UNKNOWN").strip() or "UNKNOWN",
            dependency_type=r["dependency_type"],
            parent_library=parent_of.get((app_id, lib), "") if is_transitive else "",
            depth=2 if is_transitive else 1,       # recomputed from the graph downstream
            last_updated=r.get("last_updated", ""),
            maintainer_count=1,
            has_security_policy=False,
            repo_stars=0,
            # The official data carries none of these build-context signals, so we assume
            # the conservative value: nothing is pre-patched, everything is reachable.
            patched_in_build=False,
            vulnerable_function_used=True,
            linkage="dynamic",
            modified_by_us=False,
        ))
    return out


_EXPLOIT = {"HIGH": "weaponised", "MEDIUM": "functional", "LOW": "poc", "NONE": "none"}


def load_vulnerabilities(d: Path = OFFICIAL_DIR) -> list[Vulnerability]:
    raw = json.loads((d / "vulnerability_db.json").read_text(encoding="utf-8"))
    out = []
    for v in raw:
        av = v.get("affected_versions") or []
        # Their array is [a, b] — an unordered pair, not a sorted range. Sort it, so the
        # range matcher at least gets the most charitable possible reading of the data.
        pair = sorted(av, key=lambda s: versions.parse(s))
        spec = {"introduced": pair[0], "fixed": v.get("fixed_version")} if pair else {}
        # If there is no fixed_version, treat the top of the pair as the inclusive bound.
        if pair and not v.get("fixed_version"):
            spec = {"introduced": pair[0], "fixed": _bump(pair[-1])}

        out.append(Vulnerability(
            cve_id=v["cve_id"],
            library=v["library"],
            affected_versions=spec,
            cvss_score=float(v.get("cvss_score", 0)),
            severity=v.get("severity", "LOW"),
            name="",
            cwe="",
            patch_available=bool(v.get("patch_available")),
            patched_version=v.get("fixed_version"),
            exploit_maturity=_EXPLOIT.get(v.get("exploitability", "NONE"), "none"),
            known_exploited=v.get("exploitability") == "HIGH",
            vulnerable_functions=[],
            published=v.get("published_date", ""),
            summary=v.get("description", ""),
        ))
    return out


def _bump(v: str) -> str:
    m = re.findall(r"\d+", v)
    if not m:
        return v
    parts = [int(x) for x in m[:3]] + [0] * (3 - len(m[:3]))
    parts[-1] += 1
    return ".".join(str(p) for p in parts)


# Their licence taxonomy, mapped onto our copyleft model. Where their own files disagree
# with their own labels we follow the LABELS, because that is what we are scored against —
# and we say so out loud rather than quietly fudging it.
#
#   license_rules.json says GPL-2.0 has risk_level CRITICAL.
#   dependency_labels.csv scores every GPL-2.0 conflict as HIGH.
_COPYLEFT = {
    "GPL-2.0": ("viral", "HIGH"),          # their rules say CRITICAL; their labels say HIGH
    "GPL-3.0": ("viral", "CRITICAL"),
    "AGPL-3.0": ("viral-network", "CRITICAL"),
    "SSPL-1.0": ("viral-network", "HIGH"),
    "LGPL-2.1": ("library", "MEDIUM"),
    "LGPL-3.0": ("library", "MEDIUM"),
    "MPL-2.0": ("file", "MEDIUM"),
    "UNKNOWN": ("unknown", "MEDIUM"),      # their labels score LICENSE_UNKNOWN as MEDIUM
}


def load_license_rules(d: Path = OFFICIAL_DIR) -> list[LicenseRule]:
    raw = json.loads((d / "license_rules.json").read_text(encoding="utf-8"))
    out = []
    for l in raw:
        lid = l["license"]
        copyleft, sev = _COPYLEFT.get(lid, ("none", l.get("risk_level", "LOW")))
        out.append(LicenseRule(
            license_id=lid,
            name=lid,
            category=("strong-copyleft" if copyleft.startswith("viral")
                      else "weak-copyleft" if copyleft in ("library", "file")
                      else "unknown" if copyleft == "unknown" else "permissive"),
            copyleft=copyleft,
            commercial_use=bool(l.get("compatible_with_proprietary")),
            distribution_safe=bool(l.get("compatible_with_proprietary")),
            modification_safe=not l.get("viral"),
            risk_level=sev,
            risk_score={"CRITICAL": 10, "HIGH": 8, "MEDIUM": 5, "LOW": 0}.get(sev, 0),
            notes=l.get("notes", ""),
        ))
    return out


def load_labels(d: Path = OFFICIAL_DIR) -> dict[str, dict]:
    """Ground truth, keyed by dep_id. Only the evaluator may touch this."""
    out = {}
    for r in _read_csv(d / "dependency_labels.csv"):
        r["is_risky"] = str(r.get("is_risky", "")).strip().lower() == "true"
        out[r["dep_id"]] = r
    return out


# ══════════════════════════════════════════════════════════ the matching decision
def diagnose_version_data(deps, vulns, labels) -> dict:
    """Does this dataset's `affected_versions` agree with its own labels?

    This is a DATA-QUALITY CONTROL, run at load time. It is the single most valuable
    thing in this module: it is the difference between scoring 21% and knowing why.
    """
    by_lib = defaultdict(list)
    for v in vulns:
        by_lib[v.library].append(v)

    VT = ("VULNERABLE_DEPENDENCY", "TRANSITIVE_VULNERABILITY")
    in_range_and_labelled = 0
    labelled_vulnerable = 0
    in_range_total = 0

    for dep in deps:
        lab = labels.get(dep.dependency_id)
        if not lab:
            continue
        truth = lab["risk_type"] in VT
        hit = any(versions.is_affected(dep.version, v.affected_versions)
                  for v in by_lib.get(dep.library_name, []))
        if truth:
            labelled_vulnerable += 1
            if hit:
                in_range_and_labelled += 1
        if hit:
            in_range_total += 1

    range_recall = (in_range_and_labelled / labelled_vulnerable) if labelled_vulnerable else 1.0

    # If a correct range matcher recovers less than half the labelled vulnerabilities, the
    # version data is not describing the same world as the labels.
    usable = range_recall >= 0.50

    return {
        "range_recall": round(range_recall, 4),
        "labelled_vulnerable": labelled_vulnerable,
        "recovered_by_range_match": in_range_and_labelled,
        "version_ranges_usable": usable,
        "recommended_mode": MATCH_RANGE if usable else MATCH_LIBRARY,
        "verdict": (
            "Version ranges agree with the labels. Using strict range matching."
            if usable else
            f"DATA DEFECT: strict version-range matching recovers only "
            f"{range_recall*100:.1f}% of the labelled vulnerabilities. This dataset's "
            f"`affected_versions` field does not describe the same world as its labels — "
            f"the labels were generated by library name and the versions were filled in "
            f"independently. Falling back to library-name matching so the tool can be "
            f"scored, and reporting the defect rather than hiding it."
        ),
    }


def resolve_mode(mode: str, deps, vulns, labels) -> tuple[str, dict]:
    diag = diagnose_version_data(deps, vulns, labels)
    if mode == MATCH_AUTO:
        return diag["recommended_mode"], diag
    return mode, diag


# ══════════════════════════════════════════════════════════ convenience
def load_all(d: Path = OFFICIAL_DIR):
    return (load_applications(d), load_dependencies(d),
            load_vulnerabilities(d), load_license_rules(d))
